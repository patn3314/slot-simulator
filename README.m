## 🚀 クイックスタート

1. このリポジトリをクローンまたは ZIP 展開  
2. `data/roles.csv` を UTF-8 で用意（フォーマット例は後述）  
3. `index.html` をブラウザで開く  
4. フォームに条件を入力 → **［シミュレーション開始］**  
   - 進行状況バーと別ウインドウのログで経過を確認  
5. 完了後、ページ内に統計値・ヒストグラム・CSV ダウンロードボタンが表示

---

## 🔍 リアルタイムログ

- 別ウインドウに 100G ごとのログを出力  
- 完了後は自動で閉じます（500 ms 後）

---

## 📊 出力

| セクション | 内容 |
| --- | --- |
| 統計値 | 投資額・最終メダル・収支の平均 / 中央 / 最小 / 最大 |
| ヒストグラム | 同項目 3 種を Chart.js で描画 |
| CSV | `simulation_results.csv` に各シミュレーションの詳細を保存 |

CSV 列:  
`シムNo,ゲーム数,BIG,REG,差枚,最終メダル,投資`

